package com.example.spudydev.spudy.entidades.nota.dominio;

/**
 * The type Nota.
 */
public class Nota {

    private String titulo;
    private float valor;

    /**
     * Gets titulo.
     *
     * @return the titulo
     */
    public String getTitulo() {
        return titulo;
    }

    /**
     * Sets titulo.
     *
     * @param string the string
     */
    public void setTitulo(String string) {
        this.titulo = string;
    }

    /**
     * Gets valor.
     *
     * @return the valor
     */
    public double getValor() {
        return valor;
    }

    /**
     * Sets valor.
     *
     * @param float1 the double 1
     */
    public void setValor(float float1) {
        this.valor = float1;
    }
}
