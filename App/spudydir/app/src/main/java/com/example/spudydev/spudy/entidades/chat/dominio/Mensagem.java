package com.example.spudydev.spudy.entidades.chat.dominio;

/**
 * The type Mensagem.
 */
public class Mensagem {

    private String texto;
    private String autor;

    /**
     * Gets texto.
     *
     * @return the texto
     */
    public String getTexto() {
        return texto;
    }

    /**
     * Sets texto.
     *
     * @param string the string
     */
    public void setTexto(String string) {
        this.texto = string;
    }

    /**
     * Gets autor.
     *
     * @return the autor
     */
    public String getAutor() {
        return autor;
    }

    /**
     * Sets autor.
     *
     * @param string the string
     */
    public void setAutor(String string) {
        this.autor = string;
    }
}
